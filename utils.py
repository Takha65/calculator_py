import numpy as np 
import math 
def add(a, b):
    """
    Fonction qui additionne deux nombres.
    """
    return a + b

def multipli(a, b) : 
     """
     Fonction qui multiplie deux fonctions. 
     
     """
     return a*b 

def calculFactorial(a) : 
     return math.factorial(a)

def calcul_power(base, exponent):
    """Élève 'base' à la puissance 'exponent'."""
    return np.power(base, exponent)